          <h1>The Historic Thomas Center</h1>
          <p>Following a street lined with beautiful and quirky Queen Anne style houses, 
            you'll approach an estate in the historic neighborhood of Duckpond. 
            Known as the "cultural heart" of Gainesville, The Thomas Center is a space 
            for art and history exhibits, performances, and meeting rooms for civic matters. 
            However, the most important aspect to note of the historic area is the gardens 
            adjacent to the building. Find a perfect reading perch in the gazebo, pack for 
            a picnic beneath the hundred-year-old oak, or take a mindful stroll through grassy 
            pathways and observe squirrels and canaries, all the while surrounding yourself with 
            the rich history of the memorialized grounds.
          </p>
