          <h1>Paynes Prairie</h1>
          <p>If you want to take a step away from the city altogether, but don't want to travel far, 
            Payne's Prairie is a slice of pure, wild Florida. Not only was it the state's first preserve 
            as of 1971, but it's also one of the scarce places east of the Mississippi where you can 
            still see bison roam, as they once did from coast to coast. There are dozens of miles 
            of trail to explore in the prairie, but if it's your first visit, you may want to stop 
            at the visitor's center and see the park from atop their 50 foot watchtower. If you're a 
            wildlife or bird enthusiast, the must-see La Chua trail, which is a mile of wetlandy goodness, 
            replete with basking gators, wading birds, vocal frogs and timid water snakes. A few notes of 
            caution: because it's a prairie, there aren't many trees or shady spots to be found, so be sure 
            to pack plenty of water and sunscreen. And if you come after a particularly rainy day, expect 
            many parts of the trails to be flooded.
          </p>
