          <h1>Bo Diddley Plaza</h1>
          <p>Named after a legendary musician who spent much of his later years in the area, 
            Bo Diddley Plaza is a vibrant community and family-oriented space. The distinctive 
            hub of Downtown Gainesville recently renovated to include a box office, 
            a polished stage, and a manicured grassed area for hanging out and relaxing. 
            The Union Street Farmer's Market is a local favorite, held every Wednesday 
            from 4 to 7 p.m., where diverse vendors sell their local and homemade products. 
            Concerts, dance classes and festivals happen here year-round. Bo Diddley is a casual 
            and welcoming place for all Gainesvillians, where one can hear laughter of families, 
            practice acroyoga, support other locals or enjoy a cold beer on the steps of the plaza.
          </p>
