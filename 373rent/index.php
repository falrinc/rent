          <h1>Depot Park</h1>
          <p>Despite being just over one year old, Depot Park has deep roots in Gainesville's history. 
            From railroad depot to industrial hub to public recreation area, the park's metal and 
            concrete accents hint at its storied past. The park is open and contemporary; some defining 
            features include the Rail Trail for bikers and pedestrians as well as the children's 
            playground with water jets to help cool off from the Florida heat. Depot Park houses the 
            Pop-A-Top convenience store, where you can find healthy food options and products from 
            local businesses. Although Depot Park was a hard industrial space in the past, it has been 
            transformed into a modern and inviting green area, where children can play and events, like 
            food truck rallies and art walks, are held.
          </p>
